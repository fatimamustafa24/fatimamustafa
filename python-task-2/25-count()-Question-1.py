''''25. count() 
Returns the number of occurrences of a specified element in the list. 
Question 1 :Count how many times "apple" appears in a list of fruits and print the count. 
Sample Input:fruits = ["apple", "banana", "apple", "orange"] 
Sample Output:2
'''
fruits = ["apple", "banana", "apple", "orange"] 
count = fruits.count("apple")
print(count)