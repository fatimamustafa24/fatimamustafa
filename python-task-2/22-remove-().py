'''
Removes the first occurrence of a specified element from the list. 
Question 1 :Remove "cat" from a list of animals and print the updated list. 
Sample Input:animals = ["dog", "cat", "rabbit", "cat"] 
Sample Output:["dog", "rabbit", "cat"] 
'''
animals = ["dog", "cat", "rabbit", "cat"]
animals.remove("cat")
print(animals)
